package hw;

public class TerrainFileException extends Exception {

	public TerrainFileException() {
	}

	public TerrainFileException(String message) {
		super(message);
	}

	public TerrainFileException(Throwable cause) {
		super(cause);
	}

	public TerrainFileException(String message, Throwable cause) {
		super(message, cause);
	}

	public TerrainFileException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
